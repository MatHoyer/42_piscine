#include "header.h"

void	ft_put_nb(int nbr)
{
	long	nb;

	nb = nbr;
	if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
	}
	if (nb > 9)
		ft_put_nb(nb / 10);
	ft_putchar(nb % 10 + '0');
}
