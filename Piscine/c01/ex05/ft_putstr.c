#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (i != -1)
	{
		if (str[i] != '\0')
		{
			write(1, &str[i], 1);
			i++;
		}
		else
			i = -1;
	}
}
